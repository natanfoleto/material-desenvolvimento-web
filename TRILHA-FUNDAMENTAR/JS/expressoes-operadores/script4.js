// Operadores Aritiméticos

// multiplicação *
console.log(2 * 4);

// divisão /
console.log(10 / 5);

// soma +
console.log(10 + 10);

// subtração -
console.log(100 - 30);

// resto da divisão %
console.log(11 % 11);

// incremento ++
let increment = 0;
++increment;
console.log(increment);

// decremento --
let decrement = 10;
--increment;
console.log(increment);

// exponencial **
console.log(3 ** 3);
